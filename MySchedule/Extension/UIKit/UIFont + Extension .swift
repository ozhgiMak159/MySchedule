import UIKit

extension UIFont {
    
    static func avenirNext14() -> UIFont? {
        return UIFont.init(name: "Avenir Next", size: 14)
    }
    
    static func avenirNext16() -> UIFont? {
        return UIFont.init(name: "Avenir Next", size: 16)
    }
    
    
    // Avenir Next Demi Bold
    
    static func avenirNextDemiBold14() -> UIFont? {
        return UIFont.init(name: "Avenir Next Demi Bold", size: 14)
    }
    
    static func avenirNextDemiBold16() -> UIFont? {
        return UIFont.init(name: "Avenir Next Demi Bold", size: 16)
    }
    
}
